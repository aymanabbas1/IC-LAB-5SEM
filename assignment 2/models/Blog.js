const mongoose = require('mongoose');

// Define the schema for the Blog
const blogSchema = new mongoose.Schema({
    title: {
        type: String,
        required: true
    },
    content: {
        type: String,
        required: true
    },
    author: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User', // This references the User model for the author
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    },
    updatedAt: {
        type: Date,
        default: Date.now
    }
});

// Create the Blog model
const Blog = mongoose.model('Blog', blogSchema);

// Export the Blog model
module.exports = Blog;
